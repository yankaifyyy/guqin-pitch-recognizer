def 三分损益():
    return {
        '宫': 81.0,
        '商': 72.0,
        '角': 64.0,
        '徵': 54.0,
        '羽': 48.0
    }


# 使用正调定弦5612356，计算7根弦的散音频率（正调定弦5612356）
def 正调(七弦散音频率, 调弦法):
    律数 = 调弦法()

    羽 = 七弦散音频率
    宫 = 羽 * 律数['羽'] / 律数['宫']
    商 = 羽 * 律数['羽'] / 律数['商']
    角 = 羽 * 律数['羽'] / 律数['角']
    徵 = 羽 * 律数['羽'] / 律数['徵']
    下徵 = 0.5 * 徵
    下羽 = 0.5 * 羽

    # 下标从0开始，0代表一弦，依次类推，6代表七弦
    return [下徵, 下羽, 宫, 商, 角, 徵, 羽]
